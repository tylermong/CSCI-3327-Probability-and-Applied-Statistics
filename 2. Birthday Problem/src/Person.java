/**
 * This class builds a Person object with a birthdya field, used to represent a student in the Class class.
 * 
 * @author  Tyler Mong
 * @version 1.0
 */
public class Person
{
    int birthday;

    /**
     * Constructs a Person object with the birthday set to the given parameter (in this case, a random day of the year).
     * @param setBirthday   The birthday of the person.
     */
    public Person(int setBirthday)
    {
        birthday = setBirthday;
    }
}
