public class Pokemon
{
    // Global variables
    private int hp;
    private int attack;
    private int defense;
    private int spAttack;
    private int spDefense;
    private int speed;

    // Setters and getters
    public void setHp(int userHP)
    {
        hp = userHP;
    }

    public int getHp()
    {
        return hp;
    }

    public void setAttack(int userAttack)
    {
        attack = userAttack;
    }

    public int getAttack()
    {
        return attack;
    }

    public void setDefense(int userDefense)
    {
        defense = userDefense;
    }

    public int getDefense()
    {
        return defense;
    }

    public void setSpAttack(int userSpAttack)
    {
        spAttack = userSpAttack;
    }

    public int getSpAttack()
    {
        return spAttack;
    }

    public void setSpDefense(int userSpDefense)
    {
        spDefense = userSpDefense;
    }

    public int getSpDefense()
    {
        return spDefense;
    }

    public void setSpeed(int userSpeed)
    {
        speed = userSpeed;
    }

    public int getSpeed()
    {
        return speed;
    }
}